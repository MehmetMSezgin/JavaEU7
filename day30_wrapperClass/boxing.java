package day30_wrapperClass;

public class boxing {

	public static void main(String[] args) {
		// Autoboxing
		
		Integer num1 = 123 ;
		int n = 5 ;
		Integer num2 = n ;
		
		//unboxing
		Double d1 = new Double (32.2) ; //this is just creating
		double d2= d1 ; //this is unboxing
		
		//casting is not possible with wrapper class
	}

}
