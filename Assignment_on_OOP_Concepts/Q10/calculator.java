package Assignment_on_OOP_Concepts.Q10;

public class calculator {
	
	public static int plus(int a ,int b) {
		return a+b;
	}
	
	public static int minus(int a, int b) {
		return a-b;
	}

	public static void main(String[] args) {
		calculator.plus(1, 1);
		calculator.minus(10, 1);
	}
}
