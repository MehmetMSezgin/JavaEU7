package day4_arithmeticOperators;

public class Heat_converter {

	public static void main(String[] args) {
		// F= 9 * C / 5 + 32
		double fahrenheit;
		double celcius;
		
		celcius=20;
		fahrenheit= ((9 * celcius) / 5) + 32;
		
		System.out.println( celcius + " celcius is equal to " + fahrenheit + " fahrenheit" );

	}

}
