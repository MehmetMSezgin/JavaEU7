package day4_arithmeticOperators;

public class arithmeticOperators {

	public static void main(String[] args) {
		// Addition operator
		int chairs= 150;
		System.out.println(chairs+ 5);
		int newchairsnumber= chairs+ 1 ;
		System.out.println(newchairsnumber);
		int ultimatenewchairs ;
		ultimatenewchairs= newchairsnumber + 10;
		System.out.println(ultimatenewchairs);
		
		//substraction
		int tables=120;
		int total=chairs - tables ;
		System.out.println(total);
		 
		//Multipication
		int metersquare, i1 , i2 ;
		i1=3;
		i2=4 ;
		metersquare= i1*i2;
		System.out.println(metersquare);
		
		//division
		System.out.println(10/2);
		
		//reminder
		System.out.println(10%3);
		// 10/3=3 but left 1 so "%" sign shows what left
		
		
	}

}
