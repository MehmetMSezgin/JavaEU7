package day16_Class_Objects;
		// This is a template so we did not write main method
public class Dog {
		// Class is a blueprint and has common properties and methods
	String breed ;
	int age ;
	String color ;             // Properties
	String size ;
	String name ;
	
	public void eat () {
		System.out.println(name + " is eating ");
	}
														// Methods
	public void sleep () {
		System.out.println(name + " is sleeping");
	}
	
	public void getInfo () {
		System.out.println(name + " " + age + " " + color + " " + breed + " " + size);
	}
	
	
	
	
	
	
	
	
	
	
	
	
	
	
}
