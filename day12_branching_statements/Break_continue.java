package day12_branching_statements;

public class Break_continue {

	public static void main(String[] args) {
		for (int i=1 ; i<=5 ; i++) {
			if (i==4) {
				break;            //break the loop
			}
		System.out.println(i);
			
		}
		System.out.println("///////////////");
	///////////////////////////////////////
		
		for (int i=1 ; i<=5 ; i++) {
			if (i==4) {
				continue;            //when i=4 it goes to beginning of iteration
			}						// so it does not compile number 4
		System.out.println(i);
			
		}
		
		
		
		
		
		
		
		
		
		
		
		
		
		
		
		
		
		
	}

}
